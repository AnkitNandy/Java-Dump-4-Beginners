public class GreaterLesser
{
    public static void main()
    {
        float a, b , max, min;
        a=5;b=10;
        max=(a>b)? a:b;
        min=(a<b)? a:b;
        System.out.println("The greater number is:-"+"\n"+max);
        System.out.println("The smaller number is:-"+"\n"+min);
    }
}
        